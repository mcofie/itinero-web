export const COUNTRY_BY_CODE: Record<string, CountryOption> = WORLD_COUNTRIES.reduce(
    (acc, c) => {
        acc[c.code] = c;
        return acc;
    },
    {} as Record<string, CountryOption>
);

export const COUNTRY_BY_NAME: Record<string, CountryOption> = WORLD_COUNTRIES.reduce(
    (acc, c) => {
        acc[c.label.toLowerCase()] = c;
        return acc;
    },
    {} as Record<string, CountryOption>
);

export function getCountryMeta(value?: string | null): CountryOption | null {
    if (!value) return null;
    const upper = value.toUpperCase();
    if (COUNTRY_BY_CODE[upper]) return COUNTRY_BY_CODE[upper];

    const lower = value.toLowerCase();
    if (COUNTRY_BY_NAME[lower]) return COUNTRY_BY_NAME[lower];

    return null;
}
